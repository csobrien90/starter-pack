<article class="about-this-plugin">
	<h2>About this plugin</h2>
	<p>Starter Pack is designed to be the first plugin you install on any WordPress site. It 
		provides tools to help developers work on custom plugins/themes and gets any site up 
		and running with essential built-in security features and a preloaded list of common 
		external libraries to enqueue. For the source code, installation instructions, a full list of feature, 
		a detailed changelog, and to request features or report any issues/bugs, please visit the 
		<a href="https://github.com/csobrien90/starter-pack" target="_blank">Starter Pack Plugin on GitHub</a>.
	</p>
	<p>This plugin was authored by Chad O'Brien, a web developer based in Louisville, Kentucky. 
		It was created and is maintained within the principles of 
		<a href="https://opensource.org/osd" target="_blank">the Open Source Definition</a>.
	</p>
</article>
